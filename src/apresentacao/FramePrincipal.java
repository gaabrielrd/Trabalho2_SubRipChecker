    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileFilter(new FileNameExtensionFilter("Arquivos .srt", "srt"));
        int returnValue = fileChooser.showOpenDialog(jPanel1);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            oSrt = fileChooser.getSelectedFile().getAbsoluteFile();
            jTextField1.setText(oSrt.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

